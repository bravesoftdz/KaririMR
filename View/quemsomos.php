    <h1>Quem somos</h1>
    <br />
    <div>
        <p align="justify">O KARIRI MR. Começou a ser planejado em 2015, com o intuito de facilitar as pessoas que trabalham com venda de veículo ou que estivesse interesse em comprar um, na região do Cariri, no Sul do estado do Ceara. Com o objetivo de juntar em uma única plataforma.</p>
        <br>

        <p align="justify">Num mercado em que a maioria da concorrência se limita a revender em um local físico, a solução foi criar um ambiente web com igualdade para todos. Um dos nossos principais diferenciais é a possibilidade de escolher planos diferentes do sistema, o que dará prioridade nos anúncios, ficando à frente nas buscas.</p>
        <br>

        <p align="justify">O KARIRI MR foi desenvolvido por uma equipe de cinco jovens universitários da UNILEÂO, do curso de Análise e desenvolvimento de sistemas, com o objetivo de obtenção de nota nas disciplinas Projeto Integrador I, II, III e IV.</p>
        <br>

        <h4><i>Missão</i></h4>
        <br>

        <p align="justify">“Criar para o público em geral, soluções de comprar e anunciar seu veículo, de maneira transparente, preservando uma concorrência justa dentro dos padrões da ética, dando oportunidade a todos, e garantindo lucro para o negócio”.</p>
        <br>

        <h4><i>Visão</i></h4>
        <br>

        <p align="justify">“Ser referência no mercado Caririense em serviços de anúncios automotivos focados no público geral”.</p>
        <br>

        <h4><i>Valores</i></h4>
        <br>

        <p align="justify">Comprometimento, Superação de Resultados, Melhoria contínua, Inovação, Sustentabilidade.</p>
